			<p>Over the past few years, we have been holding weekly sign choir workshops during which we teach student-interpreted ASL translations of songs. The Sign Choir has performed in various venues. We also took part in a service-learning trip, where we sent 11 Cornell students to Washington D.C. to work with a non-profit organization called Helping Educate to Advance the Rights of the Deaf, or HEARD.</p>

			<p>In addition, we have participated in the Social Justice Roundtable Series organized by Cornell Union for Disability Awareness, as well as panel discussions and workshops organized by Student Disability Services to raise awareness and educate the community of ways to increase accessibility and inclusion on campus. We have also facilitated several workshops to teach signs to other organizations on campus, such as Cornell Chorus and Alpha Phi Omega.</p>

			<p>Through these initiatives we aim not only to educate the Cornell community, but moreover to bring visibility of the need for ASL to be offered as a language option at Cornell.</p>
